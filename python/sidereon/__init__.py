"""Sidereon: Python bindings for the Sidereon GNSS positioning engine.

This package is a thin, Pythonic surface over the Rust ``sidereon`` engine. It
adds no modeling of its own; every solve returns exactly what the engine
produces. Load a precise ephemeris with :func:`load_sp3`, then run a solve:

    >>> import sidereon
    >>> sp3 = sidereon.load_sp3(open("product.sp3", "rb").read())
    >>> cfg = sidereon.SppConfig(
    ...     observations=[sidereon.SppObservation("G01", 2.1e7), ...],
    ...     t_rx_j2000_s=..., t_rx_second_of_day_s=...,
    ...     day_of_year=..., initial_guess=[0, 0, 0, 0])
    >>> sol = sidereon.solve_spp(sp3, cfg)
    >>> sol.position           # numpy array [x, y, z] in metres

Propagate a TLE over an epoch grid and compute topocentric look angles:

    >>> import numpy as np, sidereon
    >>> tle = sidereon.Tle(line1, line2)
    >>> epochs = np.asarray(times, "datetime64[us]").astype("int64")
    >>> prop = tle.propagate(epochs)          # TEME (n, 3) position/velocity
    >>> station = sidereon.GroundStation(latitude_deg=51.5, longitude_deg=-0.1)
    >>> look = tle.look_angles(station, epochs)  # az/el/range arrays

Propagate a whole fleet with the GIL released, saturating all cores:

    >>> tles = [(l1a, l2a), (l1b, l2b), ...]   # (line1, line2) per satellite
    >>> batch = sidereon.propagate_batch(tles, epochs)  # rayon-parallel
    >>> batch.position_km.shape                # (n_satellites, n_epochs, 3)

Resolve precise time scales and transform coordinates between frames:

    >>> inst = sidereon.Instant.from_utc(2020, 6, 24, 12, 34, 56.0)
    >>> inst.tt_jd, inst.ut1_jd, inst.delta_t_seconds
    >>> inst.gast_radians()                      # Greenwich apparent sidereal time
    >>> epochs = np.asarray([inst.unix_micros], dtype=np.int64)
    >>> gcrs_pos, gcrs_vel = sidereon.teme_to_gcrs(pos_km, vel_km_s, epochs)
    >>> itrs = sidereon.gcrs_to_itrs(gcrs_pos, epochs)   # Earth-fixed (ECEF)
    >>> lla = sidereon.ecef_to_geodetic(itrs)            # WGS84 lat/lon/alt

Query a precise SP3 product and the analytic Sun/Moon ephemerides:

    >>> sp3 = sidereon.load_sp3("product.sp3")       # path or bytes
    >>> axis = sp3.epochs_j2000_seconds              # node grid (n,)
    >>> interp = sp3.interpolate("G01", axis[2:] + 450.0)
    >>> interp.position_m.shape                       # (n, 3) ECEF metres
    >>> bodies = sidereon.sun_moon_ecef(epochs)       # epochs = int64 unix us
    >>> bodies.sun.shape                              # (n, 3) ECEF metres

Failures raise an exception from the :class:`SidereonError` hierarchy:
:class:`Sp3ParseError` and :class:`TleParseError` (both :class:`ParseError`) for
malformed input, and :class:`SolveError` for a solve or propagation failure.
Invalid arguments raise the built-in :class:`TypeError` / :class:`ValueError`.
"""

